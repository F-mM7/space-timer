/**
 * 移動による置換テーブル
 */
export class MoveTable {
  private permutations: Uint8Array[] = [];

  constructor() {
    this.initializePermutations();
  }

  /**
   * 全移動の置換を設定
   */
  private initializePermutations(): void {
    for (const moveName of MOVE_NAMES) {
      this.permutations.push(CUBING_JS_PERMUTATIONS[moveName]);
    }
  }

  /**
   * 置換テーブルを取得
   */
  getPermutation(moveIndex: number): Uint8Array {
    return this.permutations[moveIndex];
  }
}

